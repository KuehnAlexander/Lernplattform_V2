using System;

public class Begriff
{
	private integer ID;
	private string Name;
	private string Abkürzung;
	private string Beschreibung;
	
	public Begriff(integer ID, string Name, string Abkürzung, string Beschreibung)
	{
		this.ID = ID;
		this.Name = Name;
		this.Abkürzung = Abkürzung;
		this.Beschreibung = Beschreibung;
	}


}
